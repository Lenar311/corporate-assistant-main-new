# size_db.py — Тестовый скрипт для проверки работы с ГОСТ 2.301
from rag_chain import RAGChain

# ✅ Инициализируем RAGChain
rag = RAGChain()

# 🔍 Тестовые запросы
queries = [
    "Какие форматы листов установлены в ГОСТ 2.301?",
    "п. 4 ГОСТ 2.301 размеры форматов А0 А1 А2 А3 А4",
    "Допускается ли формат А5 и какие у него размеры?",
    "Таблица форматов ГОСТ 2.301",
]

print("🚀 Запуск тестовых запросов...\n")
for q in queries:
    print(f"❓ Вопрос: {q}")
    print(f"🤖 Ответ:\n{rag.ask(q)}")
    print("\n" + "="*80 + "\n")